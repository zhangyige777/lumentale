function hashText(text) {
  return crypto.createHash('sha256').update(text).digest('hex')
}

function normalizeHtml(html) {
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

function todayLabel() {
  return new Intl.DateTimeFormat('en-US', {
    month: 'long',
    day: 'numeric',
    year: 'numeric',
    timeZone: 'UTC',
  }).format(new Date())
}

async function readPreviousHashes() {
  try {
    const content = await fs.readFile(watchFile, 'utf8')
    const matches = [...content.matchAll(/"url": "([^"]+)"[\s\S]*?"hash": "([a-f0-9]{64})"/g)]
    return new Map(matches.map((match) => [match[1], match[2]]))
  } catch {
    return new Map()
  }
}

async function fetchSource(source, previousHashes) {
  const startedAt = Date.now()
  const controller = new AbortController()
  const timeout = setTimeout(() => controller.abort(), 25000)
  try {
    const response = await fetch(source.url, {
      headers: {
        'user-agent': 'lumentale-data-watch/1.0',
      },
      signal: controller.signal,
    })
    const html = await response.text()
    const normalized = normalizeHtml(html)
    const hash = hashText(normalized)

    return {
      ...source,
      ok: response.ok,
      status: response.status,
      checkedAt: new Date().toISOString(),
      responseMs: Date.now() - startedAt,
      contentLength: normalized.length,
      hash,
      changed: previousHashes.get(source.url) !== hash,
    }
  } catch (error) {
    return {
      ...source,
      ok: false,
      status: 0,
      checkedAt: new Date().toISOString(),
      responseMs: Date.now() - startedAt,
      contentLength: 0,
      hash: '',
      changed: true,
      error: error instanceof Error ? error.message : String(error),
    }
  } finally {
    clearTimeout(timeout)
  }
}

async function fetchTextWithCurl(url) {
  const { stdout } = await execFileAsync('curl', [
    '-L',
    '--fail',
    '--silent',
    '--show-error',
    '--max-time',
    '25',
    '-H',
    'User-Agent: Mozilla/5.0',
    url,
  ], { maxBuffer: 1024 * 1024 * 10 })

  return stdout
}

async function fetchOfficialText(url) {
  const controller = new AbortController()
  const timeout = setTimeout(() => controller.abort(), 25000)
  try {
    const response = await fetch(url, {
      headers: {
        'user-agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36',
      },
      signal: controller.signal,
    })
    if (!response.ok) {
      return fetchTextWithCurl(url)
    }
    return response.text()
  } catch {
    return fetchTextWithCurl(url)
  } finally {
    clearTimeout(timeout)
  }
}

function toTs(results) {
  return `// Generated by scripts/update-verified-data.mjs
// This file records source checks. It does not turn unverified source changes into game facts.

export const sourceWatch = ${JSON.stringify(results, null, 2)} as const

export function getLastSourceCheckDate(): string {
  return '${todayLabel()}'
}
`
}

function toVerifiedDataTs(data) {
  return `// Generated by scripts/update-verified-data.mjs
// Official-source checks used to unlock verified LumenTale content.

export const officialSourceMatches = ${JSON.stringify(data.officialSourceMatches, null, 2)} as const

export const contentUnlocks = ${JSON.stringify(data.contentUnlocks, null, 2)} as const

export function getVerifiedGameDataDate(): string {
  return '${todayLabel()}'
}
`
}

function extractExistingAnimon(content) {
  const matches = [...content.matchAll(/name: '([^']+)'/g)]
  return [...new Set(matches.map((match) => match[1]))]
}

async function updateLumentaleVerifiedData() {
  const animonContent = await fs.readFile(animonFile, 'utf8')
  const names = extractExistingAnimon(animonContent)
  const officialSources = sources.filter((source) => source.trust === 'official')
  const sourceTexts = []

  for (const source of officialSources) {
    try {
      const raw = await fetchOfficialText(source.url)
      sourceTexts.push({
        label: source.label,
        url: source.url,
        text: normalizeHtml(raw).toLowerCase(),
      })
    } catch (error) {
      sourceTexts.push({
        label: source.label,
        url: source.url,
        text: '',
        error: error instanceof Error ? error.message : String(error),
      })
    }
  }

  const officialSourceMatches = names.map((name) => {
    const lowerName = name.toLowerCase()
    const matches = sourceTexts
      .filter((source) => source.text.includes(lowerName))
      .map((source) => source.label)

    return {
      name,
      officialMatches: matches,
      status: matches.length > 0 ? 'official-name-match' : 'not-found',
    }
  })

  const matchedNames = new Set(
    officialSourceMatches
      .filter((entry) => entry.officialMatches.length > 0)
      .map((entry) => entry.name),
  )
  const today = new Date().toISOString().slice(0, 10)
  let updatedAnimon = animonContent.replace(
    /(name: '([^']+)',[\s\S]*?dataStatus: ')(community|placeholder)(',[\s\S]*?verifiedAt: ')[^']+(',)/g,
    (match, prefix, name, currentStatus, middle, suffix) => {
      if (!matchedNames.has(name)) return match
      return `${prefix}partial${middle}${today}${suffix}`
    },
  )

  if (updatedAnimon !== animonContent) {
    await fs.writeFile(animonFile, updatedAnimon, 'utf8')
  }

  const contentUnlocks = officialSourceMatches
    .filter((entry) => entry.officialMatches.length > 0)
    .map((entry) => ({
      name: entry.name,
      status: 'partial',
      reason: `${entry.name} appears in official LumenTale sources.`,
      sources: entry.officialMatches,
    }))

  await fs.writeFile(verifiedDataFile, toVerifiedDataTs({ officialSourceMatches, contentUnlocks }), 'utf8')
  return contentUnlocks
}

const previousHashes = await readPreviousHashes()
const results = []
for (const source of sources) {
  results.push(await fetchSource(source, previousHashes))
}

await fs.mkdir(path.dirname(watchFile), { recursive: true })
await fs.writeFile(watchFile, toTs(results), 'utf8')

try {
  const unlocks = await updateLumentaleVerifiedData()
  console.log(`Official LumenTale matches: ${unlocks.length}`)
} catch (error) {
  console.warn(`LumenTale verified data update skipped: ${error instanceof Error ? error.message : String(error)}`)
}


// === ACCESSORS ===

export function getAnimonBySlug(slug: string): Animon | undefined {
  return animon.find((a) => a.slug === slug)
}

export function getAllAnimon(): Animon[] {
  return animon
}

export function getVerifiedAnimon(): Animon[] {
  return animon.filter((a) => a.dataStatus !== 'placeholder')
}

export function getIndexedAnimon(): Animon[] {
  // Only confirmed or partial data with enough content to be useful.
  // A page can be indexable without a verified type if it has a documented evolution relation.
  return animon.filter((a) => {
    if (a.dataStatus === 'placeholder') return false
    if (a.dataStatus === 'community') return false
    if (a.types.length === 0 && a.evolvesFrom === null && a.evolvesTo.length === 0) return false
    // Must have some useful content beyond just a name
    if (!a.description || a.description.length < 50) return false
    return true
  })
}

export function getStarters(): Animon[] {
  return animon.filter((a) => a.isStarter)
}

export function getAnimonByType(typeSlug: string): Animon[] {
  return animon.filter((a) => a.types.includes(typeSlug))
}

export function getAnimonByAttribute(attrSlug: string): Animon[] {
  return animon.filter((a) => a.attribute === attrSlug)
}

export function getEvolutionChain(slug: string): Animon[] {
  const chain: Animon[] = []
  // Walk backwards to find base
  let current = getAnimonBySlug(slug)
  if (!current) return chain

  // Find root
  let root = current
  while (root.evolvesFrom) {
    const prev = getAnimonBySlug(root.evolvesFrom)
    if (!prev) break
    root = prev
  }

  // Walk forwards
  chain.push(root)
  let node = root
  while (node.evolvesTo.length > 0) {
    const next = getAnimonBySlug(node.evolvesTo[0])
    if (!next) break
    chain.push(next)
    node = next
  }

  return chain
}
